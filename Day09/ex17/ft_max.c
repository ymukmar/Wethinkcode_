int		ft_max(int *tab, int length)
{
	int		max;
	int		k;

	k = 1;
	if (length < 1)
		return (0);
	max = tab[0];
	while (tab[k])
	{
		if (tab[k] > max)
			max = tab[k];
		k++;
	}
	return (k);
}
