int		ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		num;

	i = 0;
	num = 0;
	while (*tab != 0)
	{
		if (f(tab[i]) == 1)
			num++;
		i++;
	}
	return (num);
}
