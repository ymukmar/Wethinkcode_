char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int		c;

	c = 0;
	while (c < n && src[c])
	{
		dest[c] = src[c];
		c++;
	}
	while (n > c)
	{
		dest[c] = '\0';
		c++;
	}
	return (dest);
}
